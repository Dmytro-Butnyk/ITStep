n = int(input('Enter n: '))

x = int(input('Enter power(1 - 7): '))

if x == 1:
    print(n**1)
elif x == 2:
    print(n**2)
elif x == 3:
    print(n**3)
elif x == 4:
    print(n**4)
elif x == 5:
    print(n**5)
elif x == 6:
    print(n**6)
elif x == 7:
    print(n**7)
else:
    print("Wrong number")